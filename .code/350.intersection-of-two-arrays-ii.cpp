class Solution {
public:
    vector<int> intersect(vector<int>& nums1, vector<int>& nums2) {
        unordered_map<int, int> m;
        vector<int> res;
        for (int a : nums1) ++m[a];
        for (int a : nums2) {
            if (m[a]-- > 0) res.push_back(a);
        }
        return res;
    }
};
